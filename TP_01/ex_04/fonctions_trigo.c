#include "fonctions_trigo.h"

//renvoie le sinus du paramètre en entrée
double sinus(double x) {
  return sin(x);
}

//renvoie le cosinus du paramètre en entrée
double cosinus(double x) {
  return cos(x);
}

//renvoie la tangente du paramètre en entrée
double tangente(double x) {
  return tan(x);
}
